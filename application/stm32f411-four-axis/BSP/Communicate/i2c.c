#include "hbird_sdk_soc.h"
#include <stdint.h>
#include "i2c.h"

/*
   I2C 引脚定义
   i2c0_scl -> [18], i2c0_sda -> [19]
*/
uint32_t I2C_SCL_PIN = (1ULL << 18);
uint32_t I2C_SDA_PIN = (1ULL << 19);

/* =========================================================================
   核心宏定义修改：模拟开漏输出 (Simulated Open-Drain)
   原理：
   1. 输出低电平 (Low)  -> 设置为输出模式，并输出 0
   2. 输出高电平 (High) -> 设置为输入模式 (利用上拉电阻拉高)

   优势：输出 High 时，引脚自动变为输入状态，可以直接读取，无需切换方向。
   ========================================================================= */

// SCL 保持推挽输出以获得更好的时钟信号质量 (也可以改为开漏，但这里为了速度保持推挽)
#define SCL_H()    gpio_write(GPIOA, I2C_SCL_PIN, 1)
#define SCL_L()    gpio_write(GPIOA, I2C_SCL_PIN, 0)

// SDA 模拟开漏核心宏
// SDA 拉低：方向设为输出(PADDIR |= PIN)，输出寄存器清零(PADOUT &= ~PIN)
#define SDA_L()    { GPIOA->PADOUT &= ~I2C_SDA_PIN; GPIOA->PADDIR |= I2C_SDA_PIN; }

// SDA 释放(拉高)：方向设为输入(PADDIR &= ~PIN)，由上拉电阻拉高。此时即为读取状态！
#define SDA_H()    { GPIOA->PADDIR &= ~I2C_SDA_PIN; }

// SDA 读取：直接读取输入寄存器 (PADIN)
#define SDA_READ() ((GPIOA->PADIN & I2C_SDA_PIN) ? 1 : 0)


/* 延时函数 */
void i2c_Delay(void) {
    for(volatile int i=0; i<400; i++);
}

/*
*********************************************************************************************************
*	函 数 名: i2c_Start
*	功能说明: CPU发起I2C总线启动信号
*********************************************************************************************************
*/
void i2c_Start(void)
{
    // 空闲状态：SDA 和 SCL 都是高（释放状态）
    SDA_H();
    SCL_H();
    i2c_Delay();

    // START: SCL 为高时，SDA 发生下降沿
    SDA_L();
    i2c_Delay();

    // 钳住总线，准备发送数据
    SCL_L();
}

/*
*********************************************************************************************************
*	函 数 名: i2c_Stop
*	功能说明: CPU发起I2C总线停止信号
*********************************************************************************************************
*/
void i2c_Stop(void)
{
    // 停止信号前，先确保 SDA 为低
    SCL_L();
    SDA_L();
    i2c_Delay();

    // STOP: SCL 为高时，SDA 发生上升沿
    SCL_H();
    i2c_Delay();
    SDA_H(); // 释放总线
    i2c_Delay();
}

/*
*********************************************************************************************************
*	函 数 名: i2c_SendByte
*	功能说明: 发送字节 (模拟开漏模式下，无需手动切换方向)
*********************************************************************************************************
*/
void i2c_SendByte(uint8_t _ucByte)
{
    uint8_t i;
    for (i = 0; i < 8; i++)
    {
        // 1. 准备数据 (此时 SCL 已经是 Low)
        if ((_ucByte & 0x80))
        {
            SDA_H(); // 发送 1 -> 释放总线 (Input Mode)
        }
        else
        {
            SDA_L(); // 发送 0 -> 拉低总线 (Output Low)
        }

        // 移位
        _ucByte <<= 1;
        i2c_Delay();

        // 2. 产生时钟脉冲
        SCL_H();
        i2c_Delay();
        SCL_L();
        i2c_Delay();
    }
}

/*
*********************************************************************************************************
*	函 数 名: i2c_ReadByte
*	功能说明: 读取字节 (模拟开漏模式下，无需手动切换方向)
*********************************************************************************************************
*/
uint8_t i2c_ReadByte(uint8_t ack)
{
    uint8_t i;
    uint8_t value = 0;

    // 关键点：读取之前，主机必须释放 SDA (即设置为 High/Input)
    SDA_H();

    for (i = 0; i < 8; i++)
    {
        value <<= 1;

        // SCL 拉高，数据有效
        SCL_H();
        i2c_Delay();

        // 直接读取 (因为 SDA_H 已经把引脚设为输入了)
        if (SDA_READ())
        {
            value++;
        }

        SCL_L();
        i2c_Delay();
    }

    // 发送应答
    if (ack == 0)
        i2c_NAck();
    else
        i2c_Ack();

    return value;
}

/*
*********************************************************************************************************
*	函 数 名: i2c_WaitAck
*	功能说明: 等待 ACK (模拟开漏模式下，无需手动切换方向)
*********************************************************************************************************
*/
uint8_t i2c_WaitAck(void)
{
    uint8_t re;
    uint16_t timeout = 0;

    // 1. 主机释放 SDA (变为输入模式)
    SDA_H();
    i2c_Delay();

    // 2. SCL 拉高，采样从机的 ACK
    SCL_H();
    i2c_Delay();

    // 3. 等待从机拉低 SDA
    while (SDA_READ())
    {
        timeout++;
        if (timeout > 2500)
        {
            i2c_Stop();
            return 1; // 失败
        }
    }

    re = 0; // 成功

    // 4. 时钟拉低，结束 ACK 周期
    SCL_L();
    i2c_Delay();

    return re;
}

/*
*********************************************************************************************************
*	函 数 名: i2c_Ack / i2c_NAck
*	功能说明: 产生应答信号
*********************************************************************************************************
*/
void i2c_Ack(void)
{
    SCL_L();
    SDA_L(); // 拉低表示 ACK
    i2c_Delay();
    SCL_H();
    i2c_Delay();
    SCL_L();
}

void i2c_NAck(void)
{
    SCL_L();
    SDA_H(); // 释放(高)表示 NACK
    i2c_Delay();
    SCL_H();
    i2c_Delay();
    SCL_L();
}
void i2c_GPIO_Config(void)
{
	/*给一个停止信号,复位I2C总线上的所有设备到待机模式*/
	i2c_Stop();
}

/*
*********************************************************************************************************
*	函 数 名: i2c_CheckDevice
*	功能说明: 检测I2C总线设备，CPU向发送设备地址，然后读取设备应答来判断该设备是否存在
*	形    参：_Address：设备的I2C总线地址
*	返 回 值: 返回值 0 表示正确， 返回1表示未探测到
*********************************************************************************************************
*/
uint8_t i2c_CheckDevice(uint8_t _Address)
{
	uint8_t ucAck;

	i2c_GPIO_Config();		/*配置GPIO*/
	i2c_Start();					/*发送启动信号*/

	/*发送设备地址+读写控制bit(0=w，1=r)bit7先传*/
	i2c_SendByte(_Address | I2C_WR);
	ucAck=i2c_WaitAck();	/*检测设备的ACK应答*/

	i2c_Stop();						/*发送停止信号*/

	return ucAck;
}
